void OLED_send_cmd(unsigned char CMD)
  { HAL_GPIO_WritePin(OLED_CS_GPIO_Port, OLED_CS_Pin, GPIO_PIN_RESET);//片选使能
    HAL_GPIO_WritePin(OLED_DC_GPIO_Port, OLED_DC_Pin, GPIO_PIN_RESET);//写命令
    HAL_SPI_Transmit(&hspi1, &CMD, sizeof(CMD), 1000);
    HAL_GPIO_WritePin(OLED_CS_GPIO_Port, OLED_CS_Pin, GPIO_PIN_SET);//关闭片选使能
  }

  void OLED_send_data(unsigned char DATA)
  {HAL_GPIO_WritePin(OLED_CS_GPIO_Port, OLED_CS_Pin, GPIO_PIN_RESET);//片选使能
   HAL_GPIO_WritePin(OLED_DC_GPIO_Port, OLED_DC_Pin, GPIO_PIN_SET);//写数据
   HAL_SPI_Transmit(&hspi1, &DATA, sizeof(DATA), 1000);
   HAL_GPIO_WritePin(OLED_CS_GPIO_Port, OLED_CS_Pin, GPIO_PIN_SET);//关闭片选使能

  }







  void Column_set(unsigned char column)
    {
  	column+=OLED_COLUMN_OFFSET;
      OLED_send_cmd(0x10|(column>>4));    //设置列地址高位
      OLED_send_cmd(0x00|(column&0x0f));   //设置列地址低位

    }
  void Page_set(unsigned char page)
    {
      OLED_send_cmd(0xb0+page);
    }
